long			ft_atol(char *s)
{
	long		i;
	char		j;

	i = 0;
	while (*s == ' ' || *s == '\f' || *s == '\r' ||
			*s == '\n' || *s == '\t' || *s == '\v')
		++*s;
	if (*s == '-' && (j = -1))
		s++;
	else if ((j = 1) && *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
		i = i * 10 + *s++ - '0';
	return (i * j);
}
